import { promises as fs } from 'node:fs'
import path from 'node:path'
import process from 'node:process'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

const DOCS_ROOT_PATH = path.resolve(__dirname, '../src/content/docs')
const INDEX_FILE_PATH = path.resolve(__dirname, '../INDEX.md')
const MARKDOWN_EXTENSIONS = new Set(['.md', '.mdx'])
const FRONTMATTER_REGEX = /^---\r?\n([\s\S]*?)\r?\n---/
const WATCH_FLAG = '--watch'

async function getAllContentFiles(directoryPath) {
  const entries = await fs.readdir(directoryPath, { withFileTypes: true })
  const files = await Promise.all(entries.map(async (entry) => {
    const absolutePath = path.join(directoryPath, entry.name)

    if (entry.isDirectory()) {
      return getAllContentFiles(absolutePath)
    }

    if (entry.isFile() && MARKDOWN_EXTENSIONS.has(path.extname(entry.name))) {
      return [absolutePath]
    }

    return []
  }))

  return files.flat()
}

function normalizeFrontmatterValue(value) {
  const trimmedValue = value.trim()

  if (!trimmedValue) {
    return ''
  }

  if (
    (trimmedValue.startsWith('"') && trimmedValue.endsWith('"')) ||
    (trimmedValue.startsWith("'") && trimmedValue.endsWith("'"))
  ) {
    return trimmedValue.slice(1, -1).trim()
  }

  return trimmedValue
}

function extractDescriptionFromFrontmatter(content) {
  const frontmatterMatch = content.match(FRONTMATTER_REGEX)
  if (!frontmatterMatch) {
    return ''
  }

  const frontmatterContent = frontmatterMatch[1]
  const lines = frontmatterContent.split(/\r?\n/)
  const descriptionLine = lines.find((line) => line.startsWith('description:'))

  if (!descriptionLine) {
    return ''
  }

  const descriptionValue = descriptionLine.slice('description:'.length)
  return normalizeFrontmatterValue(descriptionValue)
}

function createLocalFileLink(relativePath) {
  return `./src/content/docs/${relativePath.replaceAll(path.sep, '/')}`
}

function createSectionTitle(sectionName) {
  return sectionName
    .split('-')
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ')
}

async function generateIndexFile() {
  const contentFiles = await getAllContentFiles(DOCS_ROOT_PATH)

  const sections = new Map()

  for (const contentFilePath of contentFiles) {
    const fileContent = await fs.readFile(contentFilePath, 'utf8')
    const relativePath = path.relative(DOCS_ROOT_PATH, contentFilePath)
    const relativePathForLink = relativePath.replaceAll(path.sep, '/')
    const pathSegments = relativePathForLink.split('/')
    const sectionName = pathSegments.length > 1 ? pathSegments[0] : 'root'
    const fileName = path.basename(relativePathForLink)
    const description = extractDescriptionFromFrontmatter(fileContent) || 'No description provided.'

    const currentSectionEntries = sections.get(sectionName) ?? []
    currentSectionEntries.push({
      description,
      fileName,
      localLink: createLocalFileLink(relativePathForLink),
    })
    sections.set(sectionName, currentSectionEntries)
  }

  const sortedSections = [...sections.entries()]
    .map(([sectionName, entries]) => [sectionName, entries.sort((a, b) => a.fileName.localeCompare(b.fileName))])
    .sort((a, b) => a[0].localeCompare(b[0]))

  const indexLines = [
    '# Documentation Index',
    '',
    '> This file is auto-generated by `pnpm --filter @gcs-crm/documentation generate:index`.',
    '',
  ]

  for (const [sectionName, entries] of sortedSections) {
    indexLines.push(`## ${createSectionTitle(sectionName)}`, '')

    for (const entry of entries) {
      indexLines.push(`- [${entry.fileName}](${entry.localLink}) - ${entry.description}`)
    }

    indexLines.push('')
  }

  const indexContent = `${indexLines.join('\n').trimEnd()}\n`
  await fs.writeFile(INDEX_FILE_PATH, indexContent, 'utf8')
}

function shouldRegenerateFromFilePath(filePath) {
  return MARKDOWN_EXTENSIONS.has(path.extname(filePath))
}

async function startWatchMode() {
  await generateIndexFile()
  console.log('Documentation index generated. Watching for changes...')

  let debounceTimer = null

  const scheduleRegeneration = () => {
    if (debounceTimer) {
      clearTimeout(debounceTimer)
    }

    debounceTimer = setTimeout(async () => {
      try {
        await generateIndexFile()
        console.log('Documentation index updated.')
      }
      catch (error) {
        console.error('Failed to regenerate documentation index:', error)
      }
    }, 150)
  }

  fs.watch(DOCS_ROOT_PATH, { recursive: true }, (eventType, fileName) => {
    if (!fileName) {
      return
    }

    if (eventType !== 'change' && eventType !== 'rename') {
      return
    }

    const changedFilePath = fileName.toString()

    if (!shouldRegenerateFromFilePath(changedFilePath)) {
      return
    }

    scheduleRegeneration()
  })
}

const isWatchMode = process.argv.includes(WATCH_FLAG)

if (isWatchMode) {
  await startWatchMode()
}
else {
  await generateIndexFile()
}
